#![ cfg (test) ]

use super::*;

const EXAMPLE_0: & [& str] = & [
	"0,0,0,0",
	"3,0,0,0",
	"0,3,0,0",
	"0,0,3,0",
	"0,0,0,3",
	"0,0,0,6",
	"9,0,0,0",
	"12,0,0,0",
];

const EXAMPLE_1: & [& str] = & [
	"-1,2,2,0",
	"0,0,2,-2",
	"0,0,0,-2",
	"-1,2,0,0",
	"-2,-2,-2,2",
	"3,0,2,-1",
	"-1,3,2,2",
	"-1,0,-1,0",
	"0,2,1,-2",
	"3,0,0,0",
];

const EXAMPLE_2: & [& str] = & [
	"1,-1,0,1",
	"2,0,-1,0",
	"3,2,-1,0",
	"0,0,3,1",
	"0,0,-1,-1",
	"2,3,-2,0",
	"-2,2,0,0",
	"2,-2,0,-1",
	"1,-1,0,-1",
	"3,2,0,2",
];

const EXAMPLE_3: & [& str] = & [
	"1,-1,-1,-2",
	"-2,-2,0,1",
	"0,2,1,3",
	"-2,3,-2,1",
	"0,2,3,-2",
	"-1,-1,1,-2",
	"0,-2,-1,0",
	"-2,2,3,-1",
	"1,2,2,0",
	"-1,-2,0,-2",
];

#[ test ]
fn part_one () {
	let puzzle = puzzle_metadata ();
	assert_eq_ok! ("2", puzzle.part_one (EXAMPLE_0));
	assert_eq_ok! ("4", puzzle.part_one (EXAMPLE_1));
	assert_eq_ok! ("3", puzzle.part_one (EXAMPLE_2));
	assert_eq_ok! ("8", puzzle.part_one (EXAMPLE_3));
}
